class Solution:
    def searchRange(self, arr, number):
        firstIndex = None
        secondIndex = None

        lo = 0
        hi = len(arr)

        while lo<hi:
            mi = int((lo+hi)>>1)
            if arr[mi] == number and (mi==len(arr)-1 or arr[mi+1]!=number):
                secondIndex = mi
            if arr[mi]<=number:
                lo = mi+1
            elif arr[mi]>number:
                hi = mi

        lo = 0
        hi = len(arr)

        while lo<hi:
            mi = int((lo+hi)>>1)
            if arr[mi] == number and (mi==0 or arr[mi-1]!=number):
                firstIndex = mi
            if arr[mi]<number:
                lo = mi+1
            elif arr[mi]>=number:
                hi = mi

        if firstIndex == None and secondIndex==None:
            return [-1,-1]
        return [firstIndex,secondIndex]

