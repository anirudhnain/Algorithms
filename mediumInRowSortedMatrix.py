class Solution:
	def binarySearch(self, arr, number):
		lo = 0
		hi = len(arr)
		while lo<hi:
			mi = int((lo+hi)>>1)
			if arr[mi]<=number:
				lo = mi+1
			else:
				hi = mi
		return lo
		
	def medianInRowSorted(self, arr):
		if not arr:
			return 0
		nColums = len(arr[0])
		nRows = len(arr)

		minMatrix = arr[0][0]
		maxMatrix = arr[0][nColums-1]
		
		for i in range(nRows):
			minMatrix = min(minMatrix, arr[i][0])
			maxMatrix = max(maxMatrix, arr[i][nColums-1])

		lo = minMatrix
		hi = maxMatrix
		desiredIndex = int((nRows*nColums+1)>>1)

		while lo<hi:
			mi = int((lo+hi)>>1)
			indexMi = 0
			for i in range(nRows):
				indexMi += self.binarySearch(arr[i], mi)
			if indexMi<desiredIndex:
				lo = mi+1
			else:
				hi = mi
		return lo
